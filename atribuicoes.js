let x = 10; // Inicializa a variável x com o valor 10
console.log(`Valor inicial: x = ${x}`);

x += 5;    // Atribuição de adição: x = 10 + 5 → x agora é 15
console.log(`Após x += 5: x = ${x}`);

x -= 3;    // Atribuição de subtração: x = 15 - 3 → x agora é 12
console.log(`Após x -= 3: x = ${x}`);

x *= 2;    // Atribuição de multiplicação: x = 12 × 2 → x agora é 24
console.log(`Após x *= 2: x = ${x}`);

x /= 4;    // Atribuição de divisão: x = 24 ÷ 4 → x agora é 6
console.log(`Após x /= 4: x = ${x}`);

x %= 5;    // Atribuição de módulo (resto da divisão): x = 6 % 5 → x agora é 1
console.log(`Após x %= 5: x = ${x}`);

x **= 3;   // Atribuição de exponenciação: x = 1³ → x agora é 1
console.log(`Após x **= 3: x = ${x}`);
